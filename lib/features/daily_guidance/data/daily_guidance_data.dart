  /// Get all items for a specific day (1-30)
  static List<DailyGuidanceItem> getItemsForDay(int day) {
    final validDay = ((day - 1) % 30) + 1;
    return _allItems.where((item) => item.dayNumber == validDay).toList();
  }

  /// Get items for today based on day of year rotation
  static List<DailyGuidanceItem> getTodayItems() {
    final dayOfYear = DateTime.now().difference(DateTime(DateTime.now().year, 1, 1)).inDays + 1;
    final day = ((dayOfYear - 1) % 30) + 1;
    return getItemsForDay(day);
  }

  /// Get current day number
  static int get currentDayNumber {
    final dayOfYear = DateTime.now().difference(DateTime(DateTime.now().year, 1, 1)).inDays + 1;
    return ((dayOfYear - 1) % 30) + 1;
  }

