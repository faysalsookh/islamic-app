  /// Get transliteration for a specific ayah
  static String? getTransliteration(int surahNumber, int ayahNumber) {
    final key = '$surahNumber:$ayahNumber';
    return _transliterations[key];
  }

  /// Check if surah has transliterations
  static bool hasSurah(int surahNumber) {
    return _transliterations.keys.any((k) => k.startsWith('$surahNumber:'));
  }

  /// Get all transliterations for a surah
  static Map<int, String> getSurahTransliterations(int surahNumber) {
    final Map<int, String> result = {};
    for (final entry in _transliterations.entries) {
      if (entry.key.startsWith('$surahNumber:')) {
        final ayahNum = int.parse(entry.key.split(':')[1]);
        result[ayahNum] = entry.value;
      }
    }
    return result;
  }

